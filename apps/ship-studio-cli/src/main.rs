use anyhow::Result;
use cli::*;
use std::path::PathBuf;

fn main() -> Result<()> {
    use clap::Parser;
    let cli = Cli::parse();
    dispatch(cli.command)
}

fn dispatch(command: Option<Commands>) -> Result<()> {
    match command {
        None => {
            // No subcommand: show status
            run_status(None)
        }
        Some(cmd) => match cmd {
            Commands::Init { global, provider } => run_init(global, provider),
            Commands::Login => stub("login", "Run: open https://getship.dev/login"),
            Commands::Logout => stub("logout", "Removed local session token."),
            Commands::Whoami => run_whoami(),
            Commands::Use { mode, path, compile } => run_use(&mode, path, compile),
            Commands::Status { path } => run_status(path),
            Commands::Modes { local, project, cloud } => run_modes(local, project, cloud),
            Commands::Mode { action } => dispatch_mode(action),
            Commands::Compile { provider, dry_run, watch, path } => {
                run_compile_cmd(provider.as_deref(), dry_run, watch, path)
            }
            Commands::Skill { action } => dispatch_skill(action),
            Commands::Mcp { action } => dispatch_mcp(action),
            Commands::Import { path } => {
                stub("import", &format!("Would import from {:?}", path.as_deref().unwrap_or(std::path::Path::new("."))))
            }
            Commands::Export { provider, zip: _ } => {
                run_compile_cmd(Some(&provider), false, false, None)
            }
            Commands::Sync { pull: _, push: _ } => {
                stub("sync", "Cloud sync requires a Ship account. Run: ship login")
            }
            Commands::Server { .. } => {
                stub("server", "Local server (port 7701) — coming soon")
            }
        },
    }
}

// ── Init ──────────────────────────────────────────────────────────────────────

fn run_init(global: bool, provider: Option<String>) -> Result<()> {
    if global {
        paths::ensure_global_dirs()?;
        let cfg_path = paths::global_dir().join("config.toml");
        if !cfg_path.exists() {
            std::fs::write(&cfg_path, "# Ship Studio global configuration\n\n[identity]\nname = \"\"\n")?;
        }
        // README so teammates know what .ship is
        let readme = paths::global_dir().join("README.md");
        if !readme.exists() {
            std::fs::write(&readme, "# Ship\n\nPersonal AI agent configuration. See https://getship.dev\n")?;
        }
        println!("✓ initialized global config at ~/.ship/");
        println!("  Edit ~/.ship/config.toml to set your identity");
    } else {
        paths::ensure_project_dirs()?;
        let ship_toml = paths::project_ship_toml();
        if !ship_toml.exists() {
            let provider_str = provider.as_deref().unwrap_or("claude");
            std::fs::write(&ship_toml, format!(
                "# Ship Studio project configuration\n\n[project]\nproviders = [\"{provider_str}\"]\n"
            ))?;
        }
        // .gitignore for compiled artifacts
        let gitignore = paths::project_dir().join(".gitignore");
        if !gitignore.exists() {
            std::fs::write(&gitignore,
                "# Ship Studio — compiled artifacts (regenerated by ship compile)\n\
                 /secrets/\nCLAUDE.md\nGEMINI.md\nAGENTS.md\n.mcp.json\n.codex/\n.gemini/\n.cursor/\n"
            )?;
        }
        // README in .ship/
        let readme = paths::project_dir().join("README.md");
        if !readme.exists() {
            std::fs::write(&readme,
                "# Ship Configuration\n\nThis project uses [Ship](https://getship.dev) \
                 to manage AI agent configuration.\n\n\
                 Install the CLI: `curl -fsSL https://getship.dev/install | sh`\n\
                 Then: `ship use` to activate the project mode.\n"
            )?;
        }
        println!("✓ initialized .ship/ in current directory");
        println!("  providers: {}", provider.as_deref().unwrap_or("claude"));
        println!("\nNext steps:");
        println!("  ship mode create <name>   create a mode");
        println!("  ship compile               generate agent configs");
    }
    Ok(())
}

// ── Auth stubs ────────────────────────────────────────────────────────────────

fn run_whoami() -> Result<()> {
    let cfg = config::ShipConfig::load();
    match cfg.identity {
        Some(id) if !id.name.is_empty() => {
            println!("{}", id.name);
            if let Some(email) = id.email { println!("{}", email); }
        }
        _ => {
            println!("Not logged in.");
            println!("Run: ship init --global");
        }
    }
    Ok(())
}

// ── Use ───────────────────────────────────────────────────────────────────────

fn run_use(mode_id: &str, path: Option<PathBuf>, do_compile: bool) -> Result<()> {
    // Resolve the target path
    let target = path.as_deref()
        .map(std::fs::canonicalize)
        .transpose()?
        .unwrap_or_else(|| std::env::current_dir().unwrap());

    // Verify mode exists
    let ship_root = target.join(".ship");
    let mode_file = {
        let p = ship_root.join("modes").join(format!("{}.toml", mode_id));
        let g = paths::global_modes_dir().join(format!("{}.toml", mode_id));
        if p.exists() { Some(p) } else if g.exists() { Some(g) } else { None }
    };

    if mode_file.is_none() {
        anyhow::bail!(
            "Mode '{}' not found.\n  Create it with: ship mode create {}\n  Or list available: ship modes",
            mode_id, mode_id
        );
    }

    // Save to path-context
    let mut ctx = config::PathContext::load();
    ctx.paths.insert(
        target.to_string_lossy().to_string(),
        config::PathEntry { mode: Some(mode_id.to_string()), provider: None },
    );
    ctx.save()?;
    println!("✓ activated mode '{}' for {}", mode_id, target.display());

    // Optionally compile immediately
    if do_compile && ship_root.join("agents").exists() {
        run_compile_cmd(None, false, false, Some(target))?;
    }
    Ok(())
}

// ── Status ────────────────────────────────────────────────────────────────────

fn run_status(path: Option<PathBuf>) -> Result<()> {
    let target = path.as_deref()
        .map(std::fs::canonicalize)
        .transpose()?
        .unwrap_or_else(|| std::env::current_dir().unwrap());

    let ctx = config::PathContext::load();
    match ctx.active_mode_for(&target) {
        Some(mode) => {
            println!("active mode: {}", mode);
            println!("path: {}", target.display());
        }
        None => {
            println!("No active mode for {}", target.display());
            println!("Run: ship use <mode-id>");
        }
    }
    Ok(())
}

// ── Modes ─────────────────────────────────────────────────────────────────────

fn run_modes(local: bool, project: bool, cloud: bool) -> Result<()> {
    if cloud {
        println!("Cloud modes require a Ship account. Run: ship login");
        return Ok(());
    }
    let modes = paths::list_mode_ids(local, project);
    if modes.is_empty() {
        println!("No modes found.");
        println!("Create one with: ship mode create <name>");
    } else {
        for (id, scope) in &modes {
            println!("  {} [{}]", id, scope);
        }
    }
    Ok(())
}

// ── Mode subcommands ──────────────────────────────────────────────────────────

fn dispatch_mode(action: ModeCommands) -> Result<()> {
    match action {
        ModeCommands::Create { name, global } => {
            let dir = if global { paths::global_modes_dir() } else { paths::project_modes_dir() };
            std::fs::create_dir_all(&dir)?;
            let path = dir.join(format!("{}.toml", name));
            if path.exists() {
                anyhow::bail!("Mode '{}' already exists at {}", name, path.display());
            }
            std::fs::write(&path, mode::Mode::scaffold(&name))?;
            println!("✓ created mode '{}' at {}", name, path.display());
            println!("  Edit: {}", path.display());
        }
        ModeCommands::Edit { name, editor } => {
            let path = paths::find_mode_file(&name).ok_or_else(|| {
                anyhow::anyhow!("Mode '{}' not found. Create with: ship mode create {}", name, name)
            })?;
            let editor = editor
                .or_else(|| std::env::var("EDITOR").ok())
                .unwrap_or_else(|| "vi".to_string());
            std::process::Command::new(&editor).arg(&path).status()?;
        }
        ModeCommands::Delete { name } => {
            let path = paths::find_mode_file(&name).ok_or_else(|| {
                anyhow::anyhow!("Mode '{}' not found", name)
            })?;
            std::fs::remove_file(&path)?;
            println!("✓ deleted mode '{}'", name);
        }
        ModeCommands::Clone { source, target } => {
            let src_path = paths::find_mode_file(&source).ok_or_else(|| {
                anyhow::anyhow!("Source mode '{}' not found", source)
            })?;
            let is_global = src_path.starts_with(paths::global_modes_dir());
            let dst_dir = if is_global { paths::global_modes_dir() } else { paths::project_modes_dir() };
            let dst_path = dst_dir.join(format!("{}.toml", target));
            if dst_path.exists() {
                anyhow::bail!("Target mode '{}' already exists", target);
            }
            let content = std::fs::read_to_string(&src_path)?
                .replace(&format!("id = \"{}\"", source), &format!("id = \"{}\"", target))
                .replace(&format!("name = \"{}\"", source), &format!("name = \"{}\"", target));
            std::fs::write(&dst_path, content)?;
            println!("✓ cloned '{}' → '{}'", source, target);
        }
        ModeCommands::Publish { name: _ } => {
            stub("mode publish", "Publishing requires a Ship account. Run: ship login")?;
        }
    }
    Ok(())
}

// ── Compile ───────────────────────────────────────────────────────────────────

fn run_compile_cmd(
    provider: Option<&str>,
    dry_run: bool,
    watch: bool,
    path: Option<PathBuf>,
) -> Result<()> {
    let project_root = path.as_deref()
        .map(std::fs::canonicalize)
        .transpose()?
        .unwrap_or_else(|| std::env::current_dir().unwrap());

    if !project_root.join(".ship").exists() {
        anyhow::bail!(".ship/ not found. Run: ship init");
    }

    // Resolve active mode for this directory
    let ctx = config::PathContext::load();
    let active_mode = ctx.active_mode_for(&project_root).map(str::to_string);

    compile::run_compile(compile::CompileOptions {
        project_root: &project_root,
        provider,
        dry_run,
        active_mode: active_mode.as_deref(),
    })?;

    if watch {
        println!("--watch not yet implemented. Run compile manually after changes.");
    }
    Ok(())
}

// ── Skill subcommands ─────────────────────────────────────────────────────────

fn dispatch_skill(action: SkillCommands) -> Result<()> {
    match action {
        SkillCommands::List => skill::list(),
        SkillCommands::Create { id, name, description } => {
            skill::create(&id, name.as_deref(), description.as_deref())
        }
        SkillCommands::Remove { id, global } => skill::remove(&id, global),
        SkillCommands::Add { source, .. } => {
            stub("skill add", &format!("Registry install for '{}' — coming soon", source))
        }
        SkillCommands::Update => stub("skill update", "Registry updates — coming soon"),
        SkillCommands::Validate { .. } => stub("skill validate", "Spec validation — coming soon"),
        SkillCommands::Publish { .. } => {
            stub("skill publish", "Publishing requires a Ship account. Run: ship login")
        }
    }
}

// ── MCP subcommands ───────────────────────────────────────────────────────────

fn dispatch_mcp(action: McpCommands) -> Result<()> {
    match action {
        McpCommands::List => mcp::list(),
        McpCommands::Add { id, name, url, .. } => {
            let url = url.ok_or_else(|| anyhow::anyhow!("--url is required for HTTP/SSE servers"))?;
            mcp::add_http(&id, name, &url)
        }
        McpCommands::AddStdio { id, command, args, name, .. } => {
            mcp::add_stdio(&id, name, &command, args)
        }
        McpCommands::Remove { id } => mcp::remove(&id),
        McpCommands::Probe { id } => {
            stub("mcp probe", &format!("Live probing for {:?} — requires local server", id))
        }
    }
}

// ── Helpers ───────────────────────────────────────────────────────────────────

fn stub(command: &str, note: &str) -> Result<()> {
    println!("[{}] {}", command, note);
    Ok(())
}
