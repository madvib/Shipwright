pub fn run(emit: bool, check: bool) -> Result<()> {
    let output = generate();

    if emit {
        let docs_dir = std::env::current_dir()?.join("docs");
        std::fs::create_dir_all(&docs_dir)?;
        let path = docs_dir.join("surface.md");
        std::fs::write(&path, &output)?;
        println!("wrote {}", path.display());
    } else if check {
        let path = std::env::current_dir()?.join("docs").join("surface.md");
        if !path.exists() {
            bail!("docs/surface.md not found. Run: ship surface --emit");
        }
        let committed = std::fs::read_to_string(&path)?;
        if committed != output {
            eprintln!("surface drift detected: docs/surface.md is out of date.");
            eprintln!("Run: ship surface --emit   then commit docs/surface.md");
            std::process::exit(1);
        }
        println!("surface OK: docs/surface.md matches current CLI + MCP surface");
    } else {
        print!("{}", output);
    }

    Ok(())
}
