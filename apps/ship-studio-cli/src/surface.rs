// CORE_TOOLS kept in sync with apps/mcp/src/server.rs::core_tools().
// If you update one, update the other.
const CORE_TOOLS: &[&str] = &[
    "open_project",
    "create_note",
    "update_note",
    "create_adr",
    "activate_workspace",
    "create_workspace",
    "complete_workspace",
    "list_stale_worktrees",
    "set_agent",
    "list_workspaces",
    "start_session",
    "end_session",
    "log_progress",
    "list_skills",
    "create_job",
    "update_job",
    "list_jobs",
    "append_job_log",
    "claim_file",
    "get_file_owner",
    "list_events",
    "provider_matrix",
];

pub fn generate() -> String {
    let mut out = String::new();
    writeln!(out, "# Ship Surface").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "Generated by `ship surface`. Do not edit manually.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "## CLI Commands").unwrap();
    writeln!(out).unwrap();
    write_cli_tree(&mut out);
    writeln!(out).unwrap();
    writeln!(out, "## MCP Core Tools").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "Tools always available regardless of active agent:").unwrap();
    writeln!(out).unwrap();
    let mut tools: Vec<&str> = CORE_TOOLS.to_vec();
    tools.sort_unstable();
    for tool in tools {
        writeln!(out, "- `{}`", tool).unwrap();
    }
    out
}

